function addBooks(){
  for (var i = 0; i < 3 ; i++) {
    var order= i+1;
    // getting informaton from user

    var titleName =prompt("enter the book title for " + order);
    var authorName = prompt("enter the author for " + order);
    var genreName = prompt("enter the genre for " + order);

    var temptBook = {
      author: authorName,
      title: titleName,
      genre: genreName//dont use comma at last
    };
    bookArray.push(temptBook);
  }
}

